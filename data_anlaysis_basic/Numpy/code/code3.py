# -*- coding:utf-8 -*-

import numpy as np

a=np.array(range(8))

print('生成數組為:\n',a)

print('數組第1個到第6個的數字為:\n',a[0:6])
print('數組最後一個數字為:\n',a[-1])
print('數組從最後取到前面第2個為:\n',a[7:1:-1])
print('數組從第一個跳2個取的數字為:\n',a[0::2])

b=np.array([[1,2,3,4,5,6,7,8],[3,4,5,6,7,8,9,10]])

print('生成二維數組為:\n',b)
print('取第一列從第五行開始取為:\n',b[0,5:])
print('取第二列從第一行取到第五行為:\n',b[1,0:5])
print('取第二列從倒數第二行開始取為:',b[1,-2::-1])
